        // Auto slide functionality
        const container = document.querySelector('.container123');
        const items = document.querySelectorAll('.item345');
        let currentIndex = 0;
        const slideInterval = 3000; // Adjust the slide interval as needed (in milliseconds)

        function scrollToNextItem() {
            currentIndex = (currentIndex + 1) % items.length;
            container.scrollTo({
                left: items[currentIndex].offsetLeft,
                behavior: 'smooth'
            });
        }

        setInterval(scrollToNextItem, slideInterval);

